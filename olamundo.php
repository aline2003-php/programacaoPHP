//Tipos de dados em PHP

<?php
// Tipo FLOAT (número decimal)
$v = 300.5;
var_dump($v); 
// Resultado: float(300.5)

// Tipo INTEGER (número inteiro)
$v = 300;
var_dump($v); 
// Resultado: int(300)

// Tipo BOOLEAN (verdadeiro/falso)
$v = true;
var_dump($v); 
// Resultado: bool(true)

// Tipo STRING (texto)
$v = "Aline";
var_dump($v); 
// Resultado: string(5) "Aline"
?>
